def get_country_data(country_code: str, attr_key: str):
    """
    Fetch country-specific financial parameters.
    
    :param country_code: Country code (e.g., 'us', 'in', 'ca')
    :return: Dictionary containing country financial parameters or error message if not found.
    """
    country_data = COUNTRY_MAPPING.get(country_code.lower())

    if not country_data:
        return country_code

    # If locale is provided and matches, return data, otherwise return default country data
    if attr_key:
        return country_data.get(attr_key.lower())

    return ""
